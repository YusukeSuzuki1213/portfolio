      <p>悪気がないのはわかるのですが、鬱陶しいのでやめてください。</p>

      <h3>サークルを作り出そうとする</h3>
      <p>アーティストの演奏をみたい僕にとっては大変迷惑です。</p>
      <p>なぜなら、サークルを作るために、観客を容赦無くつっとばしてきくるからです。</p>
      <p>例えば、好きなアーティストの特に好きな曲で、ノリノリで曲を聴いてるときに、これをされたらマジで腹が立ちます。</p>

