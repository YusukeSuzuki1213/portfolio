      <h3>モッシュピットナイト</h3>
      <p>
        <span class="bold">「モッシュピットナイト」</span>とはYoutuberのセゴリータ三世さんが提唱しました。(おそらく)
      </p>
      <p>モッシュピットナイトの意味は下記の通りです。</p>
      <blockquote class="quote">
        <p>モッシュピットナイトとは、モッシュピット(ライブ中観客同士激しくぶつかり合う行為)の中で彼氏が彼女を守る行為。</p>
