      <blockquote class="quote">
        <p>
          コールアンドレスポンス（英: call and
          response）とは、音楽における「掛け合い」である。本来は、例えばメーンボーカルとコーラスの歌詞が呼応するなど、複数の演奏者または歌手が、前者の呼びかけに後者が応答する形でフレーズを継承して演奏・歌唱する楽式を意味する
        </p>
