      <h3>リュックを背負ってくる</h3>
      <p>これは、人が密集している場所では迷惑ですね。</p>
      <p>ステージ近くのスタンディングは人が密集することが多く、リュックのせいで、余計に場所を占領してしまいます。</p>
      <p>例えば、なるべく前でアーティストを見たい人がいるにも関わらず、リュックを背負った人がいれば、迷惑になってしまいます。</p>
      <p>基本的にフェスには、クロークと呼ばれる荷物を預ける場所があるので、そこに預けてからみにいきましょう。</p>

      <h3>爪が長い</h3>
      <p>細かいことですが、地味に迷惑です。</p>
      <p>フェスは観客の動きが激しい分、観客同士が接触することがとても多いです。</p>
      <p>
        なので爪が長いと、
        <span class="bold">他の観客を傷つけてしまう</span>恐れがあります。
      </p>
      <p>例えば、長い爪が目に入ってしまえば、最悪失明してしまいます。</p>
      <p>ライブの前のルーティーンとして、爪は短く切って行きましょう。</p>

      <h3>ヒールを履いてくる</h3>
      <p>
        これは迷惑を通り越して
        <span class="bold">大変危険</span>です。
      </p>
      <p>自分が転んで怪我をするなら全然いいのですが、他の観客を傷つけてしまう恐れがあります。</p>
      <p>例えば、下記のツイートのように、ヒールで足を踏まれて大怪我に繋がってしまうことがあります。</p>

      <p>おしゃれをしたい気持ちはわかりますが、他の観客も傷つけてしまう恐れがあるので、ヒールを履いてくるのは絶対にやめましょう。</p>

      <h3>その他</h3>
