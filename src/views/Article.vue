              target="_blank"
            >「今すぐやめろ！ライブやフェスで彼女を守る行為に全く意味はないぞ！【完全論破モッシュピットナイト】」</a>より引用
          </cite>
        </p>
      </blockquote>
      <p></p>
      <p>問題なのは、彼氏が彼女を過剰に守ろうとするあまり、他の観客を攻撃してしまう行為です。</p>
      <p>例えば、モッシュが発生した際、彼女を守ろうとして観客を肘で攻撃すると、攻撃された観客は不快な気持ちになってしまいます。</p>
      <p>気持ちはわかりますが、不要な攻撃をしてまで、彼女をかばうことはやめましょう。</p>

      <h3>大声で熱唱する</h3>
      <p>これは、場合によっては迷惑ですね。</p>
      <p>特にバラードなどの場合は、耳元で歌われてしまうと、肝心なアーティストの声が聴こえなくなってしまいます。</p>
      <p>下記ツイートにもあるように、百歩譲って、歌が上手ければいいのですが、そういう人に限って、歌が下手なんです。</p>

