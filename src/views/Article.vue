        <span class="bold">「僕が思う嫌な行為」</span>について書いてきます。下記の2つです。
      </p>
      <ul class="list-main">
        <li>古参ファンがマウントをとる</li>
        <li>サークルを作り出そうとする</li>
      </ul>
      <p>一般的には迷惑行為には当たらないかもしれないですが、個人的に嫌いな行為です。</p>
      <p>共感する人もいるのではないでしょうか。</p>
      <p>では、順番にみてきます。</p>
      <h3>古参ファンがマウントをとる</h3>
      <p>
