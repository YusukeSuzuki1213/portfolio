        <li>体臭がひどい</li>
      </ul>

      <p>ルールに縛られすぎることも良くないですが、観客全員が気持ちよくなれるように、迷惑行為はなくしていけるといいですね。</p>

      <h2>個人的に嫌な行為</h2>
